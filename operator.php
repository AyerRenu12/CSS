//multiplication
$mul=$num1*$num2;
echo $mul;

//division
$div=$num1/$num2;
echo$div;

//modulus
$mod=$num1%$num2;
echo $mod;

//exponentiation
$exp=$num1**$num2;
echo $exp;

//assignment operator
$x=20;
